m = 3.5
n = 2

print("Nilai m = %f" % m)
print("Nilai n = %d" % n) # apa fungsi %f dan %d ?
# kode diatas menjelaskan nilai bilangan desimal yang kemudian di ubah oleh fungsi %f dan %d

#ubah %f dan %d
print("Nilai m = %d" % m)
print("Nilai n = %f" % n)
# %d berfungsi untuk membulatkan bilangan desimal
# %f untuk membuat bilangan real 
x = m*n
print(" m x n = %f" % x) # hasil = 7.00000 (real floating point)
print(" m x n = %d" % x) # hasil = 7. bilangan bulat desimal
